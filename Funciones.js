var xhttp = new XMLHttpRequest();
function Main() {
    xhttp.onreadystatechange = function() {
      if (this.readyState == 4 && this.status == 200) {
       document.getElementById("demo").innerHTML = this.responseText;
      }
    };
    xhttp.open("GET", "Main.php", true);
    xhttp.send();
  }

